package com.maxxposure.app.model;

public class ImageRatio {

    private String imageRatioId;

    private String imageRatio;

    private String imageRatioCreatedAt;

    private String imageRatioUpdatedAt;

    public String getImageRatioId() {
        return imageRatioId;
    }

    public void setImageRatioId(String imageRatioId) {
        this.imageRatioId = imageRatioId;
    }

    public String getImageRatio() {
        return imageRatio;
    }

    public void setImageRatio(String imageRatio) {
        this.imageRatio = imageRatio;
    }

    public String getImageRatioCreatedAt() {
        return imageRatioCreatedAt;
    }

    public void setImageRatioCreatedAt(String imageRatioCreatedAt) {
        this.imageRatioCreatedAt = imageRatioCreatedAt;
    }

    public String getImageRatioUpdatedAt() {
        return imageRatioUpdatedAt;
    }

    public void setImageRatioUpdatedAt(String imageRatioUpdatedAt) {
        this.imageRatioUpdatedAt = imageRatioUpdatedAt;
    }
}
